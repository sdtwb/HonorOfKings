package honorOfKings;

public class PartPerson {
     public int Mp;
     public int Hp;
     public int Exp;
     public char personName;
     public int positionX;
     public int positionY;
     
     PartPerson(int mp, int hp, int exp, int x, int y, char name){
    	 Mp= mp;
    	 Hp= hp;
    	 Exp= exp;
    	 positionX= x;
    	 positionY= y;
    	 personName= name;
     }
}
